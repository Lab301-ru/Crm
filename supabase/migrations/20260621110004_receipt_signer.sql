-- ============================================================
-- PDF-квитанции: автоподстановка подписанта со стороны сервиса.
--   Печатная форма рендерится в браузере и сохраняется как PDF.
--   На стороне «сотрудник/исполнитель» автоматически подставляются
--   расшифровка подписи (имя) и факсимиле-подпись. По умолчанию —
--   имя «Юрий», подпись «Б.Ю.Г.» (настраивается в org_settings).
-- ============================================================

alter table public.org_settings
  add column if not exists receipt_signer_name text not null default 'Юрий',
  add column if not exists receipt_signer_signature text not null default 'Б.Ю.Г.';
